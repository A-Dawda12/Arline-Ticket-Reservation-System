        // TODO add your handling code here:
        String  id = txtuserid.getText();
        String firstname = txtfirstname.getText();
        String lastname = txtlastname.getText();
        String username = txtusername.getText();
        String password = txtpassword.getText();
        
        
        
        try {
            Class.forName("com.mysql.jdbc.Driver");
            
            con = DriverManager.getConnection("jdbc:mysql:///airline?serverTimezone = UTC","root","");
            pst = con.prepareStatement("Insert into user(id,firstname,lastname,username,password)values(?,?,?,?,?)");
            pst.setString(1, id);
            pst.setString(2, firstname);
            pst.setString(3, lastname);
            pst.setString(4, username);
            pst.setString(5, password);
            
            
            pst.executeUpdate();
            
            JOptionPane.showMessageDialog(null,"Registration Created.........");
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(userCreation.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex){
            Logger.getLogger(userCreation.class.getName()).log(Level.SEVERE, null, ex);
        }
        
